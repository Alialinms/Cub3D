#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	j;

	j = 0;
	i = 0;
	while (s[j] != '\0')
		j++;
	i = j - 1;
	while (i >= 0)
	{
		if (s[i] == (char)c)
		{
			return ((char *)(&s[i]));
		}
		i--;
	}
	if (c == '\0' || c == 0 || c == 1024)
		return ((char *)(&s[j]));
	return (0);
}
